import mongoose from "mongoose";

const subtodoSchema = new mongoose(
  {
    content: {
      type: String,
      required: true,
    },
    complete: {
      type: Boolean,
      default: false,
    },
    createdBy: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
    },
  },
  { timestamps: true }
);

export default Subtodo = mongoose.model("Subtodo", subtodoSchema);
